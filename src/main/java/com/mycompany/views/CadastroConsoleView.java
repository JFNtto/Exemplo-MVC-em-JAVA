    private void btnCadastrarConsoleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarConsoleActionPerformed
        String nomeConsole = txtNomeConsole.getText();
        String descricaoConsole = textAreaDescConsole.getText();
        int quantidade = Integer.parseInt(txtQuantidadeConsole.getText());
        String fabricante = txtFabricante.getText();
        
        Consoles console = new Consoles();
        if(console.cadastrar(id, nomeConsole, descricaoConsole, quantidade, fabricante)){
                JOptionPane.showMessageDialog(null, "Console cadastrado com Sucesso", "Deu certo!", 1);
        }else{
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel cadastrar", "Algo deu errado", 0);
        }
        
        
        this.dispose();
        MenuView menu = new MenuView();
        menu.setVisible(true);
    }//GEN-LAST:event_btnCadastrarConsoleActionPerformed

